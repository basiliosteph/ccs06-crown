In Chapter 6, Fox goes home without seeing Eadlyn. Hale comes back 
and is happy to know that Eadlyn wants him to make her wedding dress. 
After he leaves, she decides to tell Kile about her decision to marry him 
before talking to Henri. Kile is working in his old room, and she realizes 
that this is what he loves. She cannot take him away from it even though 
he proposes to her before she tells him about her decision. When he says 
that she cannot make him go, Eadlyn banishes him from the palace for 
one year. She says he can get an apartment and continue working on his 
housing project for the poor. In addition, he can return on special 
occasions. Eadlyn goes back to the office and has Lady Brice send Erik to 
see her.