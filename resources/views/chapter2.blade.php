In Chapter 2, Kile and Eadlyn are walking through the garden. He 
reassures her and tells Eadlyn how great she was at breakfast. He says 
that she can, “change momentum” (45). Eadlyn tells Kile what Ahern said 
about the peoples’ feelings toward her and the monarchy. She is worried 
that the people may attack now that she is in charge. She reminds him of 
how the people stormed the palace and killed her grandparents. Kile 
offers to kiss her, but she refuses, saying that she cannot be flippant with 
him. He lets Eadlyn know that he has feelings for her as well. They agree 
that is too soon to call their feelings love and that Eadlyn should continue 
with the Selection. Then they go to find something to eat.